#include "util.h"

#include <stdlib.h>
#include <string.h>

uint16_t block_to_number(const uint8_t *block, uint8_t length)
{
    char string[length+1];
    memcpy(string, block, length);
    string[length] = '\0';
    return atoi(string);
}
